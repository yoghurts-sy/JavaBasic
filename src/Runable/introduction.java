package Runable;

/** 线程的核心概念：
 *      1.线程就是独立的执行路径
 *      2.在程序运行时，即使你没有创建线程，后台也会有多个线程，如主线程（main），gc线程
 *      3.线程的运行由调度器安排，先后顺序是不能人为的干预
 *      4.同一份资源会存在资源抢夺的问题，需要加入并发控制
 *      5.线程会带来额外的开销，如cpu调度时间，并发控制开销
 *      6.每个线程在自己的工作内存交互，内存控制不当会造成数据的不一致。
 */

